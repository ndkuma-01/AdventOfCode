package Utilities.aoc22;
public class Duplex {
    double firstNum;
    double secNum;
    public Duplex(double a, double b){
        firstNum = a;
        secNum = b;
    }

    public double getFirstNum() {
        return firstNum;
    }

    public double getSecNum() {
        return secNum;
    }

    public void setFirstNum(double firstNum) {
        this.firstNum = firstNum;
    }

    public void setSecNum(double secNum) {
        this.secNum = secNum;
    }

}
